//! Trajectory and route-based position types for path following
//!
//! This file contains:
//! - TrajectoryPosition for positioning along predefined trajectories
//! - RoutePosition for positioning along route waypoints
//! - InRoutePosition for complex route-internal positioning
//! - Trajectory reference resolution and validation
//! - Path interpolation and position calculation utilities
//!
//! Contributes to project by:
//! - Supporting complex path-following and trajectory execution
//! - Enabling route-based navigation and waypoint following
//! - Providing smooth interpolation along curved paths
//! - Facilitating integration with path planning systems
//! - Supporting both geometric and parametric trajectory representations