//! World coordinate position types for absolute positioning
//!
//! This file contains:
//! - WorldPosition for absolute X,Y,Z coordinates with orientation
//! - RelativeWorldPosition for entity-relative positioning in world space
//! - GeoPosition for GPS/geographic coordinate positioning
//! - Coordinate system transformation utilities
//! - Orientation and heading calculations
//!
//! Contributes to project by:
//! - Providing absolute positioning foundation for scenario geometry
//! - Supporting both Cartesian and geographic coordinate systems
//! - Enabling precise spatial relationships and measurements
//! - Facilitating integration with mapping and GIS systems
//! - Supporting real-world coordinate system alignment and validation